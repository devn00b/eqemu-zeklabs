sub EVENT_SAY {
  if($text =~ /Hail/i) {
  $interestedLink = quest::saylink("interested"); quest::say("Hello, $name. I am the master of the forge of devn00b. He has permitted me to appear here and offer you a chance to upgrade your ornate armor! Let me know if you're 
actually $interestedLink"); quest::doanim(15);
  }
if($text=~/interested/i) {
    quest::say("Of course you are interested! Items from the gods are rare! Who wouldn't want them?");
    quest::say(" Here is how it works, you bring me: ");
    quest::say(" (1) Golden Token of Service which is sold here in bazaar");
    quest::say(" (1) Titanium Token of Bravery which comes from some high level monsters.");
    quest::say("");
    quest::say("Once you have both of those I will forge them into 1 random Ornate armor with an augment slot for you. I will only give you something you dont already have, so dont worry about that.");
  }
}

